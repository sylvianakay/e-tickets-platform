    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        response.setContentType("application/json;charset=UTF-8");
        String username = request.getParameter("username");
        String password = request.getParameter("password");
        try (PrintWriter out = response.getWriter()) {
            /* TODO output your page here. You may use following sample code. */
            EditUsersTable eut = new EditUsersTable();
            User su = eut.databaseToUsers(username, password);
            
            
            if (su == null) {
                response.setStatus(403);
            } else {
                String json = eut.userToJSON(su); //skippable, just for debugging
                                
                HttpSession session = request.getSession(true);
                session.setAttribute("user", su);
                session.setMaxInactiveInterval(30 * 60); // Session timeout: 30 minutes

                out.write(json);
                response.setStatus(200);
            }
        } catch (SQLException ex) {
            Logger.getLogger(GetUser.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(GetUser.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

